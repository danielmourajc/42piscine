#include <unistd.h>
#include <stdio.h>

void	ft_is_negative(int n)
{
	char	np;

	if (n < 0)
	{
		np = 'N';
		write(1, &np, 1);
	}
	else
	{
		np = 'P';
		write(1, &np, 1);
	}
}
